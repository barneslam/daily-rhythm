async function sendEmail(report) {
  const timestamp = new Date().toISOString();
  const logFile = path.join(__dirname, '../logs/sow-audit-' + timestamp.split('T')[0] + '.txt');

  await fs.mkdir(path.dirname(logFile), { recursive: true });
  await fs.writeFile(logFile, report);

  // Send via Gmail API
  try {
    const { google } = require('googleapis');
    const fs = require('fs').promises;
    const path = require('path');

    const credPath = path.join(process.env.HOME, '.config/galoras-gmail-creds.json');
    const creds = JSON.parse(await fs.readFile(credPath, 'utf8'));

    const gmail = google.gmail({
      version: 'v1',
      auth: new google.auth.OAuth2(
        creds.client_id,
        creds.client_secret,
        creds.redirect_uri
      )
    });

    const emailContent = `From: barnes@thestrategypitch.com\nTo: barnes@thestrategypitch.com\nSubject: Galoras SOW Daily Audit - ${new Date().toLocaleDateString()}\n\n${report}`;

    const base64Email = Buffer.from(emailContent).toString('base64');

    await gmail.users.messages.send({
      userId: 'me',
      requestBody: {
        raw: base64Email
      }
    });

    console.log(`[${timestamp}] Email sent to: barnes@thestrategypitch.com`);
  } catch (emailError) {
    console.warn(`[${timestamp}] Email send failed (${emailError.message}). Report logged to: ${logFile}`);
  }

  return logFile;
}

