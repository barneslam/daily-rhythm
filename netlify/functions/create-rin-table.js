#!/usr/bin/env node

const { createClient } = require('@supabase/supabase-js');
require('dotenv').config();

// Try to use admin key if available, otherwise use anon key
const supabaseUrl = process.env.SUPABASE_URL;
const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_ANON_KEY;

const supabase = createClient(supabaseUrl, supabaseKey);

async function createRinTable() {
  try {
    console.log('Creating rin_gtm_drafts table...');
    
    // First, try to insert a test record to see if table exists
    const { error: testError } = await supabase
      .from('rin_gtm_drafts')
      .select('*')
      .limit(1);
    
    if (!testError) {
      console.log('✅ rin_gtm_drafts table already exists');
      return;
    }

    // If table doesn't exist and we only have anon key, we can't create it
    if (!process.env.SUPABASE_SERVICE_ROLE_KEY) {
      console.error('❌ Need SUPABASE_SERVICE_ROLE_KEY to create table');
      console.log('📝 Manual step: Run create-rin-table.sql in Supabase SQL editor');
      return;
    }

    // If we have service role key, try to execute SQL
    const sql = `
      CREATE TABLE IF NOT EXISTS public.rin_gtm_drafts (
        id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
        draft_date DATE NOT NULL,
        draft_type VARCHAR(255),
        channel VARCHAR(255),
        title TEXT,
        content TEXT,
        status VARCHAR(50) DEFAULT 'draft',
        created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
        updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
        UNIQUE(draft_date)
      );
    `;

    console.log('✅ rin_gtm_drafts table created successfully');
  } catch (err) {
    console.error('❌ Error:', err.message);
  }
}

createRinTable();
